vector<int> g[2222];

int32_t main(){
    fastio;
    int n,m; sf2(n, m);
    map<string, int> M;
    map<int, string> revM;
    int idx = 1;
    while(n--){
        string a,b; sf2(a, b);
        if(!M.count(a)) M[a] = idx,revM[idx] = a,idx++;
        if(!M.count(b)) M[b] = idx,revM[idx] = b,idx++;
        g[M[a]].push_back(M[b]);
        g[M[b]].push_back(M[a]);
    }
    while(m--){
        string a,b; sf2(a, b);
        queue<int> Q;
        vi dist(2222, -1),par(2222, 0);
        dist[M[a]] = 0;
        Q.push(M[a]);
        while(Q.size()){
            auto cur = Q.front(); Q.pop();
            if(cur == M[b]) break;
            for(auto nxt : g[cur]){
                if(dist[nxt] != -1) continue;
                dist[nxt] = dist[cur] + 1;
                par[nxt] = cur;
                Q.push(nxt);
            }
        }
        string ret = "";
        int x = M[b];
        while(x != M[a]){
            ret += revM[x][0];
            x = par[x];
        }
        ret += revM[x][0];
        reverse(ret.begin(), ret.end());
        cout << ret << "\n";
    }
    return 0;
}