int Check(int x, int y){
	int a = 0,b = 0;
	for(int i = 0; i < 8; i++){
		for(int j = 0; j < 8; j++){
			if(board[i + x][j + y] != WB[i][j]){
				a++;
			}
			if(board[i + x][j + y] != BB[i][j]) {
				b++;
			}
		}
	}
	return min(a, b);
}

